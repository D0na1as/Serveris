public class Statistic {

    private int caps;
    private int gloves;
    private int goggles;
    private int masks;
    private int shoe_covers;
    private int suits;

    public int getCaps() {
        return caps;
    }

    public void setCaps(int caps) {
        this.caps = caps;
    }

    public int getGloves() {
        return gloves;
    }

    public void setGloves(int gloves) {
        this.gloves = gloves;
    }

    public int getGoggles() {
        return goggles;
    }

    public void setGoggles(int goggles) {
        this.goggles = goggles;
    }

    public int getMasks() {
        return masks;
    }

    public void setMasks(int masks) {
        this.masks = masks;
    }

    public int getShoe_covers() {
        return shoe_covers;
    }

    public void setShoe_covers(int shoe_covers) {
        this.shoe_covers = shoe_covers;
    }

    public int getSuits() {
        return suits;
    }

    public void setSuits(int suits) {
        this.suits = suits;
    }
}
